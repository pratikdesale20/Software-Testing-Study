package Pattern;

public class Eg_4 {

	public static void main(String[] args)
	{
		//*****
		//****
		//***
		//**
		//*
		
// rows=5    1st row condition star=5
		
		int star=5;
		// rows== outer for loop
		
	for(int i=1;i<=5;i++)
	{
		// inner for loop
		for (int j=1;j<=star;j++)
		{
			System.out.print("*");
		}
		System.out.println();
		star--;
	}
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
