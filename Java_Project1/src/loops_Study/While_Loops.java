package loops_Study;

public class While_Loops {

	public static void main(String[] args)
	{
		// I want to print Pratik Desale 8 times
		// start P=1  condition P<=8  updation P++
		
		int P=1;
		while (P<=8)
		{
			System.out.println("Pratik Desale");
			P++;
			
		}
	System.out.println("==========================================");	
   //   I want table 10
   // start i=9  condition i<=90  updation i+9
		int i=10;
		while (i<=100)
		{
			System.out.println(i);
			i=i+10;
		}

	}
           
              
}
