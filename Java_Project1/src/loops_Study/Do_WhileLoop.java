package loops_Study;

public class Do_WhileLoop {

	public static void main(String[] args)
	{
		// I want to print Independence Day 5 times 
		// start d=1  condition d<=5 updation d++

		int d=1;
		do
		{
			System.out.println("Independence Day");
			d++;
			
		}
		while(d<=5);
		
		System.out.println("=======================================");
		
		//  write table of 8
		// start i=8  condition i<=80 updation i+8
		
		int i=8;
		do
		{
			System.out.println(i);
			i=i+8;
		
		}
		while(i<=80);
		
		
		
		
		
	}
		
		
		
		
		
	}


